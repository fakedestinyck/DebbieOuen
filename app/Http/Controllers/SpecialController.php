    public function upload() {
        $message = $_GET["m"];
        $duplicate = Confess::where('confess', $message)->get();
        if (count($duplicate) == 0) {
            Confess::create([
                "confess" => $message
            ]);
            return array(
                "status" => 1
            );
        } else {
            return array(
                "status" => -400,
                "err_msg" => "已存在"
            );
        }
    }
